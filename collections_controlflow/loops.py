# Iterators and loops

# Iterators pass over each separate values in an iterable object
# e.g for loops, while loops

# Iterators can be used to pull out individual items of lists and dictionaries

# For Loop

# Syntax ~ " for <x> in <iteratable>: "
#                   indented block
#           pulling back from indented ends the loop

# Using a list

crazy_landlords = ['Jane', 'Mike', 'Cruella']

for landlord in crazy_landlords:
    print(f'Hey {landlord}!')